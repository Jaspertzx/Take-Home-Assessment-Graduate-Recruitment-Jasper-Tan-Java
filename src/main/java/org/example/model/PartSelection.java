package org.example.model;

/**
 * Enums representing the different parts of an application that a user can select.
 * This is typically used to control the flow of the application based on user input.
 */
public enum PartSelection {

    /**
     * Represents the selection of the first part of the application.
     */
    ONE,

    /**
     * Represents the selection of the second part of the application.
     */
    TWO,

    /**
     * Represents the selection to exit the application.
     */
    EXIT
}
